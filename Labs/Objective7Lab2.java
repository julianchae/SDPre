public class Objective7Lab2{
  public static void main(String[]args){
    int counter = 0;
    //starting number is 0
    while(counter <=20){
      //states that loop still runs when counter less than or equal to 20

      System.out.println(counter);
      //prints out number, first number is 0
      counter = counter +1;
      //adds 1 to number, first number is zero, second number would be 2, etc
      //loop continues until 20 is reached.
    }
    }
}
